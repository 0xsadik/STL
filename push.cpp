#include <bits/stdc++.h>
using namespace std;

int main()
{
    // push
    int arr[100];
    vector<int> v;
    // way to push data within an array
    arr[0] = 0;
    arr[2] = 1;
    arr[3] = 3;

    // way to push data within vector
    v.push_back(1); // v[0]
    v.push_back(2); // v[1]
    v.push_back(3); // v[2]
    v.push_back(5); // v[3]
}